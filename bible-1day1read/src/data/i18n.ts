export function t(locale: Locale, key: string, params?: Record<string, string | number>): string {
  let str = translations[locale][key] ?? translations.ko[key] ?? key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      str = str.replace(`{${k}}`, String(v));
    });
  }
  return str;
}
